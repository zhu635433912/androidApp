public class MyExpandableAdapter extends BaseExpandableListAdapter {

    private List<String> groupArray;
    private List<List<SubjectEntity>> childArray;
    private Context mContext;

    public MyExpandableAdapter(Context context, List<String> groupArray, List<List<SubjectEntity>> childArray){
        mContext = context;
        this.groupArray = groupArray;
        this.childArray = childArray;
    }

    @Override
    public int getGroupCount() {
        return groupArray.size();
    }

    @Override
    public int getChildrenCount(int groupPosition) {
        return childArray.get(groupPosition).size();
    }

    @Override
    public Object getGroup(int groupPosition) {
        return groupArray.get(groupPosition);
    }

    @Override
    public Object getChild(int groupPosition, int childPosition) {
        return childArray.get(groupPosition).get(childPosition);
    }

    @Override
    public long getGroupId(int groupPosition) {
        return groupPosition;
    }

    @Override
    public long getChildId(int groupPosition, int childPosition) {
        return childPosition;
    }

    @Override
    public boolean hasStableIds() {
        return true;
    }

    @Override
    public View getGroupView(int groupPosition, boolean isExpanded, View convertView, ViewGroup parent) {
        View view = convertView;
        GroupHolder holder = null;
        if(view == null){
            holder = new GroupHolder();
            view = LayoutInflater.from(mContext).inflate(R.layout.course_group, null);
            holder.groupName = (TextView)view.findViewById(R.id.group_text);
            view.setTag(holder);
        }else{
            holder = (GroupHolder)view.getTag();
        }

        holder.groupName.setText(groupArray.get(groupPosition));

        return view;
    }

    @Override
    public View getChildView(int groupPosition, int childPosition, boolean isLastChild, View convertView, ViewGroup parent) {
        View view = convertView;
        ChildHolder holder = null;
        if(view == null){
            holder = new ChildHolder();
            view = LayoutInflater.from(mContext).inflate(R.layout.course_child, null);
            holder.childName = (TextView)view.findViewById(R.id.child_text);
            view.setTag(holder);
        }else{
            holder = (ChildHolder)view.getTag();
        }

        holder.childName.setText(childArray.get(groupPosition).get(childPosition).getSubjectName());

        return view;
    }

    @Override
    public boolean isChildSelectable(int groupPosition, int childPosition) {
        return true;
    }

    class GroupHolder{
        public TextView groupName;

    }

    class ChildHolder{
        public TextView childName;

    }
}